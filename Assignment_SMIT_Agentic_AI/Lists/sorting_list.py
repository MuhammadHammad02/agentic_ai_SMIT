# Make a list of random numbers. Print the list before and after sorting it using the .sort() method.

if __name__ == "__main__":
    random_numbers = [42, 7, 19, 3, 25]

    print("Original list of random numbers:")
    print(random_numbers)  # Output the initial list of random numbers

    # Sort the list
    random_numbers.sort()

    print("Sorted list of random numbers:")
    print(random_numbers)  # Output the sorted list of random numbers
    